export async function generateMetadata({ params }: { params: { slug: string } }): Promise<Metadata> {
  const post = blogPosts[params.slug as keyof typeof blogPosts]

  if (!post) {
    return {
      title: "Post Not Found",
    }
  }

  return {
    title: `${post.title} - PostContent Blog`,
    description: post.excerpt,
  }
}

export default function BlogPostPage({ params }: { params: { slug: string } }) {
  const post = blogPosts[params.slug as keyof typeof blogPosts]

  if (!post) {
    notFound()
  }

  return (
    <div className="min-h-screen bg-background">
      <AppNavigation isAuthenticated={false} />

      <article className="mx-auto max-w-4xl px-4 py-12 sm:px-6 lg:px-8">
        <Link href="/blog">
          <Button variant="ghost" className="mb-8">
            <ArrowLeft className="mr-2 h-4 w-4" />
            Back to Blog
          </Button>
        </Link>

        <header className="mb-8">
          <div className="mb-4 flex flex-wrap items-center gap-4 text-sm text-muted-foreground">
            <div className="flex items-center gap-2">
              <User className="h-4 w-4" />
              <span>{post.author}</span>
            </div>
            <div className="flex items-center gap-2">
              <Calendar className="h-4 w-4" />
              <span>{new Date(post.date).toLocaleDateString()}</span>
            </div>
            <div className="flex items-center gap-2">
              <Clock className="h-4 w-4" />
              <span>{post.readTime}</span>
            </div>
          </div>

          <h1 className="text-4xl font-bold tracking-tight sm:text-5xl">{post.title}</h1>
          <p className="mt-4 text-xl text-muted-foreground">{post.excerpt}</p>
        </header>

        <div className="prose prose-lg dark:prose-invert max-w-none">
          {post.content.split("\n").map((paragraph, index) => {
            if (paragraph.startsWith("# ")) {
              return <h1 key={index}>{paragraph.replace("# ", "")}</h1>
            }
            if (paragraph.startsWith("## ")) {
              return <h2 key={index}>{paragraph.replace("## ", "")}</h2>
            }
            if (paragraph.startsWith("**") && paragraph.endsWith("**")) {
              return <h3 key={index}>{paragraph.replace(/\*\*/g, "")}</h3>
            }
            if (paragraph.trim().startsWith("-")) {
              return <li key={index}>{paragraph.replace(/^- /, "")}</li>
            }
            if (paragraph.match(/^\d+\./)) {
              return <li key={index}>{paragraph.replace(/^\d+\. /, "")}</li>
            }
            if (paragraph.trim() === "") {
              return <br key={index} />
            }
            return <p key={index}>{paragraph}</p>
          })}
        </div>

        <div className="mt-12 border-t pt-8">
          <h3 className="text-lg font-semibold mb-4">Ready to create content like this?</h3>
          <p className="text-muted-foreground mb-4">
            Generate on-brand posts for all your platforms in seconds with PostContent.
          </p>
          <Link href="/signup">
            <Button size="lg">Try it free</Button>
          </Link>
        </div>
      </article>
    </div>
  )
}
